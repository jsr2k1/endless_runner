using UnityEngine;
using System.Collections;

public class SpawnScript : MonoBehaviour
{
	public GameObject[] obj;
	public int mult;

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void Start()
	{
		Spawn();
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void Spawn()
	{
		int rand_num = Random.Range(0, obj.Length);
		Instantiate(obj[rand_num], transform.position, Quaternion.identity);
		Invoke("Spawn", mult);
	}
}
